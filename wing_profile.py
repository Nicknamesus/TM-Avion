CHORD = 210.0


page_w, page_h = A4
margin_left = 20 * mm
origin_x = margin_left
origin_y = page_h / 2 + 2 * mm  # chord line slightly above center

output_path = "X:/programming/clarky_21cm_template.pdf"
c = canvas.Canvas(output_path, pagesize=landscape(A4))

c.setLineWidth(0.7)
c.setStrokeColorRGB(0.55, 0.55, 0.55)
c.setDash(4, 3)
c.line(origin_x, origin_y, origin_x + CHORD*mm, origin_y)
c.setDash()

# --- % chord tick marks ---
c.setLineWidth(0.6)
c.setStrokeColorRGB(0.55, 0.55, 0.55)
c.setFillColorRGB(0.4, 0.4, 0.4)
c.setFont("Helvetica", 6)
for pct in range(0, 110, 10):
    tx = origin_x + pct/100 * CHORD * mm
    c.line(tx, origin_y - 2*mm, tx, origin_y + 2*mm)
    c.drawCentredString(tx, origin_y + 3*mm, f"{pct}%")

# --- Airfoil profile ---
c.setLineWidth(1.5)
c.setStrokeColorRGB(0, 0, 0)
c.setFillColorRGB(0, 0, 0)

def to_pt(x_mm, y_mm):
    return origin_x + x_mm * mm, origin_y + y_mm * mm

# Upper surface
path = c.beginPath()
px, py = to_pt(*upper[0])
path.moveTo(px, py)
for pt in upper[1:]:
    path.lineTo(*to_pt(*pt))
c.drawPath(path, stroke=1, fill=0)

# Lower surface
path = c.beginPath()
px, py = to_pt(*lower[0])
path.moveTo(px, py)
for pt in lower[1:]:
    path.lineTo(*to_pt(*pt))
c.drawPath(path, stroke=1, fill=0)

tx = origin_x + 0.28 * CHORD * mm  # 28% chord where thickness peaks
ty_top = origin_y + 18.9 * mm
ty_bottom = origin_y - 5.7 * mm
c.setLineWidth(0.5)
c.setStrokeColorRGB(0.5, 0.5, 0.5)
c.setDash(2, 2)
c.line(tx, ty_bottom, tx, ty_top)

c.save()
print("Done:", output_path)